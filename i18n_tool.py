def to_camel_case(text):
    """Convert a Portuguese string to a camelCase key name."""
    # Clean the string
    clean = text.strip().rstrip('.!?:')
    # Remove special chars, keep alphanumeric and spaces
    clean = re.sub(r'[^a-zA-ZÀ-ÿ0-9\s]', '', clean)
    # Split into words
    words = clean.split()
    if not words:
        return None
    
    # Map common Portuguese words to shorter English equivalents for key names
    word_map = {
        'erro': 'error', 'ao': '', 'de': '', 'da': '', 'do': '', 'das': '', 'dos': '',
        'no': 'in', 'na': 'in', 'nos': 'in', 'nas': 'in',
        'um': 'a', 'uma': 'a', 'uns': '', 'umas': '',
        'o': '', 'a': '', 'os': '', 'as': '',
        'para': 'for', 'com': 'with', 'sem': 'without',
        'que': 'that', 'este': 'this', 'esta': 'this',
        'seu': 'your', 'sua': 'your', 'seus': 'your', 'suas': 'your',
        'meu': 'my', 'minha': 'my', 'meus': 'my', 'minhas': 'my',
        'não': 'not', 'sim': 'yes',
        'e': 'and', 'ou': 'or',
        'é': 'is', 'são': 'are', 'foi': 'was', 'será': 'will',
        'tem': 'has', 'ter': 'have',
        'por': 'by', 'em': 'in',
        'já': 'already', 'ainda': 'yet',
        'mais': 'more', 'menos': 'less',
        'muito': 'very', 'pouco': 'little',
        'novo': 'new', 'nova': 'new', 'novos': 'new', 'novas': 'new',
        'nenhum': 'no', 'nenhuma': 'no',
        'tudo': 'all', 'todos': 'all', 'todas': 'all',
    }
    
    # Use English translation if available for key generation
    en_text = PT_TO_EN.get(text, text)
    en_clean = re.sub(r'[^a-zA-Z0-9\s]', '', en_text)
    en_words = en_clean.split()
    
    if en_words:
        # Generate camelCase from English
        result = en_words[0].lower()
        for w in en_words[1:]:
            result += w.capitalize()
        return result
    
    return None


def generate_key(pt_text, existing_keys_set):
    """Generate a unique camelCase key for a Portuguese string."""
    key = to_camel_case(pt_text)
    if not key or len(key) < 2:
        # Fallback: use hash
        key = 'str' + hashlib.md5(pt_text.encode()).hexdigest()[:8]
    
    # Ensure uniqueness
    base_key = key
    counter = 2
    while key in existing_keys_set:
        key = f"{base_key}{counter}"
        counter += 1
    
    return key


